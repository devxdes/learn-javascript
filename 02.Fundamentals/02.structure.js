// Semi colon is used for separation of statements

// This piece of code looks messy and complicated
console.log('Hello'); console.log('World');

// Looks more readable and neat
console.log('More');
console.log('Readable');

// If semicolon is not used, then line break will do same as semicolon
console.log('Wow!!!')
console.log('Awesome JavaScript');

// In most cases, a newline implies a semicolon. But “in most cases” does not mean “always”!

console.log(3
    +
    3
);

